=head1 DESCRIPTION

Unison is database of non-redundant features and an extensive collection
of precomputed features. It was specifically designed to facilitate
sequence mining ("show me sequences with these properties"), which is a
somewhat more difficult problem than merely computing features for a given
sequence.

